public class Homework1 {
	public static void main(String[] args) {
		
		System.out.println("-----------------------------------");
		System.out.println(" 영문자 모음의 아스키 코드 값");
		System.out.println("-----------------------------------");
		
		// char을 int로 명시적형변환하게 되면 해당 char의 아스키코드값을 출력한다.
		System.out.println("A = " + (int)'A');
		System.out.println("E = " + (int)'A');
		System.out.println("I = " + (int)'A');
		System.out.println("O = " + (int)'A');
		System.out.println("U = " + (int)'A');
		
		System.out.println("-----------------------------------");
	}
}
